/**
 * Writes clean local .env.local files (no VERCEL=1, no Blob).
 * Run via: vercel env run -e production -- node scripts/write-local-env.mjs
 */

import fs from "fs";
import path from "path";

const root = path.join(import.meta.dirname, "..");
const storageEnv = path.join(root, ".env.storage");

function readStorageSecret() {
  if (!fs.existsSync(storageEnv)) return "jude-local-dev-session-secret";
  for (const line of fs.readFileSync(storageEnv, "utf8").split(/\r?\n/)) {
    const match = line.match(/^JUDE_SESSION_SECRET=(.+)$/);
    if (match) return match[1].replace(/^"|"$/g, "");
  }
  return "jude-local-dev-session-secret";
}

const sessionSecret = process.env.JUDE_SESSION_SECRET || readStorageSecret() || "jude-local-dev-session-secret";

const sharedLines = [
  "# Jude local dev — generated by npm run setup:local",
  "# Uses ./data/ on disk for accounts (not Vercel Blob).",
  "# Demo: http://localhost:3002  Marketing: http://localhost:3001",
  "",
  `JUDE_SESSION_SECRET=${sessionSecret}`,
  "",
];

const demoLines = [
  ...sharedLines,
  `OPENAI_API_KEY=${process.env.OPENAI_API_KEY || ""}`,
  `ELEVENLABS_API_KEY=${process.env.ELEVENLABS_API_KEY || ""}`,
  "",
  `ELEVENLABS_VOICE_ID_GOOD=${process.env.ELEVENLABS_VOICE_ID_GOOD || "NsgfkY3cyxRG0efoRKYW"}`,
  `ELEVENLABS_VOICE_ID_EVIL=${process.env.ELEVENLABS_VOICE_ID_EVIL || "jEPqu2Z6cBH4GJyan8ZY"}`,
  "",
  "# Optional Gmail OAuth for local:",
  "# GOOGLE_REDIRECT_URI=http://localhost:3002/api/integrations/gmail/callback",
  "",
];

const marketingLines = [
  ...sharedLines,
  "# Marketing site shares accounts with demo via ./data/ and JUDE_SESSION_SECRET",
  "",
];

fs.writeFileSync(path.join(root, "apps/demo/.env.local"), demoLines.join("\n"));
fs.writeFileSync(path.join(root, "apps/marketing/.env.local"), marketingLines.join("\n"));

const hasOpenAi = Boolean(process.env.OPENAI_API_KEY);
const hasEleven = Boolean(process.env.ELEVENLABS_API_KEY);

console.log("Wrote apps/demo/.env.local");
console.log("Wrote apps/marketing/.env.local");
console.log("");
console.log(`OPENAI_API_KEY: ${hasOpenAi ? "ok" : "MISSING — voice AI will not work"}`);
console.log(`ELEVENLABS_API_KEY: ${hasEleven ? "ok" : "MISSING — Jude cannot speak"}`);
console.log(`JUDE_SESSION_SECRET: ok`);
console.log("");
console.log("Next: npm run 67  (demo → http://localhost:3002)");
console.log("      npm run dev:marketing  (→ http://localhost:3001)");

if (!hasOpenAi || !hasEleven) {
  process.exitCode = 1;
}
